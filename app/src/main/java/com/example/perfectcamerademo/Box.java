package com.example.perfectcamerademo;

public class Box {
     float x;
     float y;
     float width;
     float height;
     float prop;
     int label;

    public Box(float x, float y, float width, float height, float prop, int label) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.prop = prop;
        this.label = label;
    }
}
